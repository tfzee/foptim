bool is_reg(fir::ValueR val) {
  if (val.is_constant()) {
    auto consti = val.as_constant();
    if (consti->is_int()) {
      return false;
    }
    if (consti->is_global()) {
      return true;
    }
    if (consti->is_func()) {
      return true;
    }
    fmt::println("{}", val);
    ASSERT(false);
    std::abort();
  } else {
    return true;
  }
}
