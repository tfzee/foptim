void move_patterns(IRVec<Pattern> &pats) {
  using Node = Pattern::Node;
  using InstrType = fir::InstrType;
  using NodeType = Pattern::NodeType;

  auto IntCmpNode = Node{NodeType::Instr, InstrType::ICmp, (u32)0};
  auto SelectNode = Node{NodeType::Instr, InstrType::SelectInstr, (u32)0};

  pats.push_back(Pattern{
      .nodes = {IntCmpNode, SelectNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        // handle dumb ? 1 : 0 or ? 0 : 1 patterns that can get generated by
        // optimizations but sometimes arnt cleaned up
        fir::Instr cmp_instr = res.matched_instrs[0];
        fir::Instr slct_instr = res.matched_instrs[1];

        if (!slct_instr->value_type->is_int() ||
            !slct_instr->args[1].is_constant() ||
            !slct_instr->args[2].is_constant()) {
          return false;
        }

        auto c1 = slct_instr->args[1].as_constant()->as_int();
        auto c2 = slct_instr->args[2].as_constant()->as_int();
        auto dir1 = (c1 == 1 && c2 == 0);
        auto dir2 = (c1 == 0 && c2 == 1);
        if (!dir1 && !dir2) {
          return false;
        }
        auto res_arg =
            valueToArg(fir::ValueR(slct_instr), res.result, data.alloc);
        auto arg1 = valueToArg(cmp_instr->args[0], res.result, data.alloc);
        auto arg2 = valueToArg(cmp_instr->args[1], res.result, data.alloc);
        GJumpSubtype op = GJumpSubtype::icmp_eq;

        switch ((fir::ICmpInstrSubType)cmp_instr->get_instr_subtype()) {
          case fir::ICmpInstrSubType::SLT:
            op = dir1 ? GJumpSubtype::icmp_slt : GJumpSubtype::icmp_sge;
            break;
          case fir::ICmpInstrSubType::ULT:
            op = dir1 ? GJumpSubtype::icmp_ult : GJumpSubtype::icmp_uge;
            break;
          case fir::ICmpInstrSubType::NE:
            op = dir1 ? GJumpSubtype::icmp_ne : GJumpSubtype::icmp_eq;
            break;
          case fir::ICmpInstrSubType::EQ:
            op = dir1 ? GJumpSubtype::icmp_eq : GJumpSubtype::icmp_ne;
            break;
          case fir::ICmpInstrSubType::SGT:
            op = dir1 ? GJumpSubtype::icmp_sgt : GJumpSubtype::icmp_sle;
            break;
          case fir::ICmpInstrSubType::UGT:
            op = dir1 ? GJumpSubtype::icmp_ugt : GJumpSubtype::icmp_ule;
            break;
          case fir::ICmpInstrSubType::UGE:
            op = dir1 ? GJumpSubtype::icmp_uge : GJumpSubtype::icmp_ult;
            break;
          case fir::ICmpInstrSubType::ULE:
            op = dir1 ? GJumpSubtype::icmp_ule : GJumpSubtype::icmp_ugt;
            break;
          case fir::ICmpInstrSubType::SGE:
            op = dir1 ? GJumpSubtype::icmp_sge : GJumpSubtype::icmp_slt;
            break;
          case fir::ICmpInstrSubType::SLE:
            op = dir1 ? GJumpSubtype::icmp_sle : GJumpSubtype::icmp_sgt;
            break;
          case fir::ICmpInstrSubType::INVALID:
            UNREACH();
          case fir::ICmpInstrSubType::MulOverflow:
            if (!dir1) {
              return false;
            }
            op = GJumpSubtype::icmp_mul_overflow;
            break;
          case fir::ICmpInstrSubType::AddOverflow:
            if (!dir1) {
              return false;
            }
            op = GJumpSubtype::icmp_add_overflow;
            break;
        }
        // Do a little cheating
        res_arg.reg.ty = Type::Int8;
        res.result.emplace_back(op, res_arg, arg1, arg2);

        return true;
      }});
  pats.push_back(Pattern{
      .nodes = {IntCmpNode, SelectNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        fir::Instr cmp_instr = res.matched_instrs[0];
        fir::Instr slct_instr = res.matched_instrs[1];

        switch ((fir::ICmpInstrSubType)cmp_instr->get_instr_subtype()) {
          case fir::ICmpInstrSubType::MulOverflow:
          case fir::ICmpInstrSubType::AddOverflow:
            // TODO: impl
            return false;
          default:
        }
        auto res_arg =
            valueToArg(fir::ValueR(slct_instr), res.result, data.alloc);
        if (res_arg.is_vec_reg() || res_arg.ty == Type::Int8) {
          return false;
        }
        auto c1 = valueToArg(cmp_instr->args[0], res.result, data.alloc);
        auto c2 = valueToArg(cmp_instr->args[1], res.result, data.alloc);
        auto arg1 = valueToArg(slct_instr->args[1], res.result, data.alloc);
        auto arg2 = valueToArg(slct_instr->args[2], res.result, data.alloc);
        GCMovSubtype op = GCMovSubtype::cmov;

        switch ((fir::ICmpInstrSubType)cmp_instr->get_instr_subtype()) {
          case fir::ICmpInstrSubType::SGT:
            op = GCMovSubtype::cmov_sgt;
            break;
          case fir::ICmpInstrSubType::SLT:
            op = GCMovSubtype::cmov_slt;
            break;
          case fir::ICmpInstrSubType::ULT:
            op = GCMovSubtype::cmov_ult;
            break;
          case fir::ICmpInstrSubType::SGE:
            op = GCMovSubtype::cmov_sge;
            break;
          case fir::ICmpInstrSubType::SLE:
            op = GCMovSubtype::cmov_sle;
            break;
          case fir::ICmpInstrSubType::NE:
            op = GCMovSubtype::cmov_ne;
            break;
          case fir::ICmpInstrSubType::EQ:
            op = GCMovSubtype::cmov_eq;
            break;
          case fir::ICmpInstrSubType::UGT:
            op = GCMovSubtype::cmov_ugt;
            break;
          case fir::ICmpInstrSubType::UGE:
            op = GCMovSubtype::cmov_uge;
            break;
          case fir::ICmpInstrSubType::ULE:
            op = GCMovSubtype::cmov_ule;
            break;
          case fir::ICmpInstrSubType::MulOverflow:
          case fir::ICmpInstrSubType::AddOverflow:
          case fir::ICmpInstrSubType::INVALID:
            UNREACH();
        }

        res.result.emplace_back(GBaseSubtype::mov, res_arg, arg2);
        res.result.emplace_back(op, res_arg, arg1, c1, c2);
        return true;
      }});
}
void memory_patterns(IRVec<Pattern> &pats) {
  using Node = Pattern::Node;
  using InstrType = fir::InstrType;
  using NodeType = Pattern::NodeType;

  auto IntAddNode = Node{NodeType::Instr, InstrType::BinaryInstr,
                         (u32)fir::BinaryInstrSubType::IntAdd};
  auto IntMulNode = Node{NodeType::Instr, InstrType::BinaryInstr,
                         (u32)fir::BinaryInstrSubType::IntMul};
  auto StoreNode = Node{NodeType::Instr, InstrType::StoreInstr, 0};
  auto LoadNode = Node{NodeType::Instr, InstrType::LoadInstr, 0};

  pats.push_back(Pattern{
      .nodes = {LoadNode, IntAddNode, StoreNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 0},
                {.from_instr = 1, .to_instr = 2, .to_arg = 1}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        auto load_instr = res.matched_instrs[0];
        auto add_instr = res.matched_instrs[1];
        auto store_instr = res.matched_instrs[2];
        auto add_ty = convert_type(add_instr.get_type());
        if (add_ty >= Type::Float32) {
          // cant do it on vector regs
          return false;
        }
        if (!add_instr->args[1].is_constant()) {
          return false;
        }
        auto consti = add_instr->args[1].as_constant();
        if (!consti->is_int()) {
          return false;
        }
        if (load_instr->args[0] != store_instr->args[0]) {
          return false;
        }
        auto store_ty = convert_type(store_instr->get_type());
        if (convert_type(load_instr->get_type()) != store_ty) {
          return false;
        }
        auto consti_val = consti->as_int();
        auto ptr = valueToArg(load_instr->args[0], res.result, data.alloc);
        assert(ptr.isReg());
        res.result.emplace_back(
            GArithSubtype::add2, MArgument::MemB(ptr.reg, store_ty),
            MArgument{(u64)std::bit_cast<u128>(consti_val)});
        return true;
      }});
  pats.push_back(Pattern{
      .nodes = {IntMulNode, IntAddNode, LoadNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 1},
                {.from_instr = 1, .to_instr = 2, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        auto mul_instr = res.matched_instrs[0];
        auto add_instr = res.matched_instrs[1];
        auto load_instr = res.matched_instrs[2];
        auto load_ty = convert_type(load_instr.get_type());
        auto add_ty = convert_type(add_instr.get_type());
        if (add_ty >= Type::Float32) {
          // cant do it on vector regs
          return false;
        }
        auto add_ty_size = get_size(add_ty);
        if (add_ty_size != 8 && add_ty_size != 4) {
          return false;
        }
        if (!is_reg(add_instr->args[0]) || !is_reg(mul_instr->args[0]) ||
            !mul_instr->args[1].is_constant()) {
          return false;
        }
        auto consti = mul_instr->args[1].as_constant();
        if (!consti->is_int()) {
          return false;
        }
        auto consti_val = consti->as_int();

        switch (consti_val) {
          default: {
            return false;
          }
          case 1:
            consti_val = 0;
            break;
          case 2:
            consti_val = 1;
            break;
          case 4:
            consti_val = 2;
            break;
          case 8:
            consti_val = 3;
            break;
        }

        // $1 = $0 * C
        // R = $2 + $1
        // where $0 and $2 must be regs and C in [1,2,4,8]
        auto res_reg =
            valueToArg(fir::ValueR(load_instr), res.result, data.alloc);
        auto base = valueToArg(add_instr->args[0], res.result, data.alloc);
        auto indx = valueToArg(mul_instr->args[0], res.result, data.alloc);
        ASSERT(base.isReg());
        ASSERT(indx.isReg());
        res.result.emplace_back(
            GBaseSubtype::mov, res_reg,
            MArgument::MemBIS(base.reg, indx.reg, consti_val, load_ty));
        return true;
      }});
  pats.push_back(Pattern{
      .nodes = {IntMulNode, IntAddNode, StoreNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 1},
                {.from_instr = 1, .to_instr = 2, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        auto mul_instr = res.matched_instrs[0];
        auto add_instr = res.matched_instrs[1];
        auto store_instr = res.matched_instrs[2];
        auto store_ty = convert_type(store_instr.get_type());
        auto add_ty = convert_type(add_instr.get_type());
        if (add_ty >= Type::Float32) {
          // cant do it on vector regs
          return false;
        }
        auto add_ty_size = get_size(add_ty);
        if (add_ty_size != 8 && add_ty_size != 4) {
          return false;
        }
        if (!is_reg(add_instr->args[0]) || !is_reg(mul_instr->args[0]) ||
            !mul_instr->args[1].is_constant()) {
          return false;
        }
        auto consti = mul_instr->args[1].as_constant();
        if (!consti->is_int()) {
          return false;
        }
        auto consti_val = consti->as_int();

        switch (consti_val) {
          default: {
            return false;
          }
          case 1:
            consti_val = 0;
            break;
          case 2:
            consti_val = 1;
            break;
          case 4:
            consti_val = 2;
            break;
          case 8:
            consti_val = 3;
            break;
        }

        // $1 = $0 * C
        // R = $2 + $1
        // where $0 and $2 must be regs and C in [1,2,4,8]
        auto value = valueToArg(store_instr->args[1], res.result, data.alloc);
        auto base = valueToArg(add_instr->args[0], res.result, data.alloc);
        auto indx = valueToArg(mul_instr->args[0], res.result, data.alloc);
        if (!base.isReg() || !indx.isReg()) {
          return false;
        }
        // ASSERT(base.isReg());
        // ASSERT(indx.isReg());
        res.result.emplace_back(
            GBaseSubtype::mov,
            MArgument::MemBIS(base.reg, indx.reg, consti_val, store_ty), value);
        return true;
      }});
  pats.push_back(Pattern{
      .nodes = {IntAddNode, LoadNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        ASSERT(res.matched_instrs[0].is_valid());
        ASSERT(res.matched_instrs[1].is_valid());

        auto add_instr = res.matched_instrs[0];
        auto load_instr = res.matched_instrs[1];

        auto res_reg =
            valueToArg(fir::ValueR(load_instr), res.result, data.alloc);
        auto load_ty = convert_type(load_instr.get_type());
        auto add_ty = convert_type(add_instr.get_type());
        if (add_ty >= Type::Float32) {
          // cant do it on vector regs
          return false;
        }

        auto a0 = valueToArg(add_instr->args[0], res.result, data.alloc);

        if (add_instr->args[1].is_constant() && a0.isImm()) {
          auto c1 = add_instr->args[1].as_constant();
          if (c1->is_global()) {
            auto repl =
                valueToArgPtr(add_instr->args[1], Type::Int64, data.alloc);
            ASSERT(repl.type == MArgument::ArgumentType::MemLabel);
            res.result.emplace_back(
                GBaseSubtype::mov, res_reg,
                MArgument::MemLO(repl.label, a0.imm, load_ty));
            return true;
          }
        }
        auto a1 = valueToArg(add_instr->args[1], res.result, data.alloc);
        if (add_instr->args[0].is_constant() && a1.isImm()) {
          auto c1 = add_instr->args[0].as_constant();
          if (c1->is_global()) {
            auto repl =
                valueToArgPtr(add_instr->args[0], Type::Int64, data.alloc);
            ASSERT(repl.type == MArgument::ArgumentType::MemLabel);
            res.result.emplace_back(
                GBaseSubtype::mov, res_reg,
                MArgument::MemLO(repl.label, a1.imm, load_ty));
            return true;
          }
        }
        // if (a0.isImm() && add_instr->args[0].is_constant() &&
        //     add_instr->args[0].as_constant()->as_int() < 0) {
        //   return false;
        // }
        // if (a1.isImm() && add_instr->args[1].is_constant() &&
        //     add_instr->args[1].as_constant()->as_int() < 0) {
        //   return false;
        // }

        if (a0.isReg() && a1.isImm()) {
          res.result.emplace_back(GBaseSubtype::mov, res_reg,
                                  MArgument::MemOB(a1.imm, a0.reg, load_ty));
        } else if (a0.isImm() && a1.isReg()) {
          res.result.emplace_back(GBaseSubtype::mov, res_reg,
                                  MArgument::MemOB(a0.imm, a1.reg, load_ty));
        } else if (a0.isReg() && a1.isReg()) {
          auto max_type = std::max(a0.reg.ty, a1.reg.ty);
          auto a0_ext = a0.reg;
          auto a1_ext = a1.reg;
          a0_ext.ty = max_type;
          a1_ext.ty = max_type;
          res.result.emplace_back(GBaseSubtype::mov, res_reg,
                                  MArgument::MemBI(a0_ext, a1_ext, load_ty));
        } else {
          return false;
        }
        return true;
      }});
  pats.push_back(Pattern{
      .nodes = {IntAddNode, IntAddNode, StoreNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 1},
                {.from_instr = 1, .to_instr = 2, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        ASSERT(res.matched_instrs[0].is_valid());
        ASSERT(res.matched_instrs[1].is_valid());
        ASSERT(res.matched_instrs[2].is_valid());

        auto add0_instr = res.matched_instrs[0];
        auto add1_instr = res.matched_instrs[1];
        auto store_instr = res.matched_instrs[2];

        auto add_ty = convert_type(add1_instr.get_type());
        if (add_ty >= Type::Float32) {
          // cant do it on vector regs
          return false;
        }
        auto store_ty = convert_type(store_instr.get_type());
        auto a00 = valueToArg(add0_instr->args[0], res.result, data.alloc);
        auto a01 = valueToArg(add0_instr->args[1], res.result, data.alloc);

        auto a10 = valueToArg(add1_instr->args[0], res.result, data.alloc);
        auto value = valueToArg(store_instr->args[1], res.result, data.alloc);

        if (a00.isReg() && a01.isImm() && a10.isReg()) {
          res.result.emplace_back(
              GBaseSubtype::mov,
              MArgument::MemOBI(a01.imm, a00.reg, a10.reg, store_ty), value);
        } else if (a00.isImm() && a01.isReg() && a10.isReg()) {
          res.result.emplace_back(
              GBaseSubtype::mov,
              MArgument::MemOBI(a00.imm, a01.reg, a10.reg, store_ty), value);
        } else {
          return false;
        }
        return true;
      }});
  pats.push_back(Pattern{
      .nodes = {IntAddNode, StoreNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        ASSERT(res.matched_instrs[0].is_valid());
        ASSERT(res.matched_instrs[1].is_valid());

        // return false;
        auto add_instr = res.matched_instrs[0];
        auto add_ty = convert_type(add_instr.get_type());
        if (add_ty >= Type::Float32) {
          // cant do it on vector regs
          return false;
        }
        auto store_instr = res.matched_instrs[1];

        auto store_ty = convert_type(store_instr.get_type());
        auto value = valueToArg(store_instr->args[1], res.result, data.alloc);

        auto a0 = valueToArg(add_instr->args[0], res.result, data.alloc);
        if (add_instr->args[1].is_constant() && a0.isImm()) {
          auto c1 = add_instr->args[1].as_constant();
          if (c1->is_global()) {
            auto repl =
                valueToArgPtr(add_instr->args[1], Type::Int64, data.alloc);
            ASSERT(repl.type == MArgument::ArgumentType::MemLabel);
            res.result.emplace_back(
                GBaseSubtype::mov,
                MArgument::MemLO(repl.label, a0.imm, store_ty), value);
            return true;
          }
          if (c1->is_int() && c1->as_int() < 0) {
            return false;
          }
        }

        auto a1 = valueToArg(add_instr->args[1], res.result, data.alloc);
        if (add_instr->args[0].is_constant() && a1.isImm()) {
          auto c1 = add_instr->args[0].as_constant();
          if (c1->is_global()) {
            auto repl =
                valueToArgPtr(add_instr->args[0], Type::Int64, data.alloc);
            ASSERT(repl.type == MArgument::ArgumentType::MemLabel);
            res.result.emplace_back(
                GBaseSubtype::mov,
                MArgument::MemLO(repl.label, a1.imm, store_ty), value);
            return true;
          }
        }
        if (a0.isImm() && add_instr->args[0].is_constant() &&
            add_instr->args[0].as_constant()->as_int() < 0) {
          return false;
        }
        if (a1.isImm() && add_instr->args[1].is_constant() &&
            add_instr->args[1].as_constant()->as_int() < 0) {
          return false;
        }

        if (a0.isReg() && a1.isImm()) {
          res.result.emplace_back(GBaseSubtype::mov,
                                  MArgument::MemOB(a1.imm, a0.reg, store_ty),
                                  value);
        } else if (a0.isImm() && a1.isReg()) {
          res.result.emplace_back(GBaseSubtype::mov,
                                  MArgument::MemOB(a0.imm, a1.reg, store_ty),
                                  value);
        } else if (a0.isReg() && a1.isReg()) {
          res.result.emplace_back(GBaseSubtype::mov,
                                  MArgument::MemBI(a0.reg, a1.reg, store_ty),
                                  value);
        } else {
          return false;
        }
        return true;
      }});
}

void cjmp_patterns(IRVec<Pattern> &pats) {
  using Node = Pattern::Node;
  using InstrType = fir::InstrType;
  using NodeType = Pattern::NodeType;
  auto ICMPNode = Node{NodeType::Instr, InstrType::ICmp, 0};
  auto FCMPNode = Node{NodeType::Instr, InstrType::FCmp, 0};
  auto CondBranchNode = Node{NodeType::Instr, InstrType::CondBranchInstr, 0};

  pats.push_back(Pattern{
      .nodes = {ICMPNode, CondBranchNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        auto cmp_instr = res.matched_instrs[0];
        auto branch_instr = res.matched_instrs[1];

        auto sub_type = (fir::ICmpInstrSubType)cmp_instr->get_instr_subtype();

        // first we check if we can output a simplified version
        if (sub_type != fir::ICmpInstrSubType::SLT &&
            sub_type != fir::ICmpInstrSubType::SGE &&
            sub_type != fir::ICmpInstrSubType::SLE &&
            sub_type != fir::ICmpInstrSubType::SGT &&
            sub_type != fir::ICmpInstrSubType::NE &&
            sub_type != fir::ICmpInstrSubType::EQ &&
            sub_type != fir::ICmpInstrSubType::ULT &&
            sub_type != fir::ICmpInstrSubType::ULE &&
            sub_type != fir::ICmpInstrSubType::UGT &&
            sub_type != fir::ICmpInstrSubType::UGE) {
          fmt::println("Failed to smartly match cmp {} + branch\n",
                       cmp_instr->get_instr_subtype());
          return false;
        }

        auto &bb_with_args = branch_instr->bbs[0];
        auto target_bb = branch_instr->bbs[0].bb;
        auto v1 = valueToArg(cmp_instr->args[0], res.result, data.alloc);
        auto v2 = valueToArg(cmp_instr->args[1], res.result, data.alloc);

        // auto comp_ty = v1_orig.ty;

        // auto v1_reg = data.alloc.get_new_register(VRegInfo{comp_ty});
        // auto v2_reg = data.alloc.get_new_register(VRegInfo{comp_ty});
        // auto v1 = MArgument{v1_reg, comp_ty};
        // auto v2 = MArgument{v2_reg, comp_ty};

        // TODO: this is a issue with lifetimes
        // res.result.emplace_back(GBaseSubtype::mov, v1, v1_orig);
        // res.result.emplace_back(GBaseSubtype::mov, v2, v2_orig);

        ASSERT(bb_with_args.args.size() == target_bb->args.size());
        // ASSERT(bb_with_args.args.size() == 0);
        generate_bb_args(bb_with_args, res, data);

        if (sub_type == fir::ICmpInstrSubType::SLT) {
          res.result.push_back(
              MInstr::cJmp_slt(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::SGT) {
          res.result.push_back(
              MInstr::cJmp_sgt(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::ULT) {
          res.result.push_back(
              MInstr::cJmp_ult(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::ULE) {
          res.result.push_back(
              MInstr::cJmp_ule(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::UGT) {
          res.result.push_back(
              MInstr::cJmp_ugt(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::UGE) {
          res.result.push_back(
              MInstr::cJmp_uge(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::SGE) {
          res.result.push_back(
              MInstr::cJmp_sge(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::SLE) {
          res.result.push_back(
              MInstr::cJmp_sle(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::EQ) {
          res.result.push_back(
              MInstr::cJmp_eq(v1, v2, data.bbs[bb_with_args.bb]));
        } else if (sub_type == fir::ICmpInstrSubType::NE) {
          res.result.push_back(
              MInstr::cJmp_ne(v1, v2, data.bbs[bb_with_args.bb]));
        } else {
          UNREACH();
        }

        {
          auto bb2_with_args = branch_instr->bbs[1];
          auto target_bb2 = branch_instr->bbs[1].bb;
          ASSERT(bb2_with_args.args.size() == target_bb2->args.size());
          // ASSERT(bb2_with_args.args.size() == 0);
          generate_bb_args(bb2_with_args, res, data);
          res.result.push_back(MInstr::jmp(data.bbs[bb2_with_args.bb]));
        }
        return true;
      }});
  pats.push_back(Pattern{
      .nodes = {FCMPNode, CondBranchNode},
      .edges = {{.from_instr = 0, .to_instr = 1, .to_arg = 0}},
      .generator = [](MatchResult &res, ExtraMatchData &data) {
        auto cmp_instr = res.matched_instrs[0];
        auto branch_instr = res.matched_instrs[1];

        auto sub_type = (fir::FCmpInstrSubType)cmp_instr->get_instr_subtype();

        auto &bb_with_args = branch_instr->bbs[0];
        auto target_bb = branch_instr->bbs[0].bb;
        auto v1 = valueToArg(cmp_instr->args[0], res.result, data.alloc);
        auto v2 = valueToArg(cmp_instr->args[1], res.result, data.alloc);

        ASSERT(bb_with_args.args.size() == target_bb->args.size());
        generate_bb_args(bb_with_args, res, data);

        // TODO: we can ofcourse support this
        if (sub_type == fir::FCmpInstrSubType::IsNaN) {
          return false;
        }

        res.result.push_back(
            MInstr::cJmp_flt(v1, v2, data.bbs[bb_with_args.bb], sub_type));

        {
          auto bb2_with_args = branch_instr->bbs[1];
          auto target_bb2 = branch_instr->bbs[1].bb;
          ASSERT(bb2_with_args.args.size() == target_bb2->args.size());
          generate_bb_args(bb2_with_args, res, data);
          res.result.push_back(MInstr::jmp(data.bbs[bb2_with_args.bb]));
        }
        return true;
      }});
}

